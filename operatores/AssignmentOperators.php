<?php 
   $a = 10;

   // Assign
   $b = $a;  // $b now equals 10
   
   // Add and assign
   $b += 5;  // $b now equals 15
   
   // Subtract and assign
   $b -= 3;  // $b now equals 12
   
   // Multiply and assign
   $b *= 2;  // $b now equals 24
   
   // Divide and assign
   $b /= 4;  // $b now equals 6
   
   // Modulus and assign
   $b %= 2;  // $b now equals 0
   

