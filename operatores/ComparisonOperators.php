<?php 
   $a = 10;
   $b = 5;
   
   // Equal to
   $result = ($a == $b);  // Result: false
   
   // Not equal to
   $result = ($a != $b);  // Result: true
   
   // Identical to (equal and of the same type)
   $result = ($a === $b);  // Result: false
   
   // Not identical to (not equal or not of the same type)
   $result = ($a !== $b);  // Result: true
   
   // Greater than
   $result = ($a > $b);  // Result: true
   
   // Less than
   $result = ($a < $b);  // Result: false
   
   // Greater than or equal to
   $result = ($a >= $b);  // Result: true
   
   // Less than or equal to
   $result = ($a <= $b);  // Result: false
   

?>